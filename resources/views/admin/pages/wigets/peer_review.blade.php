    <h2 class="text-2xl font-bold tracking-widest uppercase">Peer Review Process</h2>
    <p>All submissions undergo a rigorous double-blind peer-review process to ensure objectivity and maintain the highest academic integrity. The steps involved are:</p>
    <ol>
        <li><strong>Initial Screening:</strong> The editorial board checks for relevance and compliance with submission guidelines.</li>
        <li><strong>Reviewer Assignment:</strong> Manuscripts are sent to two or more independent reviewers.</li>
        <li><strong>Review Feedback:</strong> Reviewers evaluate the quality, originality, and significance of the work.</li>
        <li><strong>Decision Making:</strong> Based on reviewer feedback, the editorial team decides to accept, revise, or reject the submission.</li>
    </ol>
</div>
